## FOR LOOP
print('My name is')
for i in range(5):
    print('Jimmy Fives Times (' + str(i) + ')')

print('My name is')
for f in range(5):
    print('Jimmy Fives Times (' + str(f) + ')')

## WHILE LOOP
print('My name is')
i = 0
while i < 5:
    print('Jimmy Fives Times ('+ str(i) + ')')
    i = i + 1

## The 2 loops above do the same thing. It's just that one is while loop. Another is for loop. 


